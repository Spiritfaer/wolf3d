#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t i;

	i = ft_strlen(s);
	while (*(s + i) != (unsigned char)c)
		if (!i--)
			return (NULL);
	return ((char*)(s + i));
}

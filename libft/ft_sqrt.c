int	ft_sqrt(int nb)
{
	int i;

	i = 1;
	if (nb == 0 && nb <= 2147395600)
		return (nb);
	while (nb >= i)
	{
		if (nb / i == i && nb % i == 0)
			return (i);
		i++;
	}
	return (0);
}
